# Both single and double quotes are valid
str0 = "Welcome"
str1 = 'Welcome'
print(str0)
print(str1)

str2 = """Hello there.
We wish you 'Welcome'"""
print(str2)

s = 'Welcome to Python'
print(s) # Displays  Welcome to Python

print(s[0]) # W (Displays 0th character)

print(s[3:7]) # come (Displays from 3rd to 6th characters)

print(s[-1]) # n (Displays first  character from last)

print(s*2)
